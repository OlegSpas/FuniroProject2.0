


////First Photo/////////////////////////////////////////////////////
const FirstPhotoButton = document.querySelector('.funiro_one img');
const FirstScreenPhoto = document.querySelector('.info__first');
const FirstCloseScreen = document.querySelector('.first-close__info');
const FirstBackground = document.querySelector('.first__background')



FirstPhotoButton.addEventListener('click', function(){
    FirstScreenPhoto.classList.remove('OfflineScreen');
    FirstScreenPhoto.classList.toggle('OnlineScreen');
    FirstBackground.classList.toggle('visible__background');
})


FirstCloseScreen.addEventListener('click', function(){
    FirstScreenPhoto.classList.remove('OnlineScreen');
    FirstScreenPhoto.classList.toggle('OfflineScreen');
    FirstBackground.classList.remove('visible__background');
})


